namespace WhileTrue.Controls
{
    /// <summary>
    /// Defines how extra space is distributed between the control and its label
    /// </summary>
    public enum DialogPanelModeStyle
    {
        /// <summary>
        /// Size is distributed proportional to the needed size of the label and the control
        /// </summary>
        Proportional,
        /// <summary>
        /// Space is only assigned to the control
        /// </summary>
        GrowControl
    }
}